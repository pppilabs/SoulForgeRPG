"""Стартовое меню проекта."""


class GameMenu:
    """Хранит основные пункты меню."""

    def __init__(self) -> None:
        self.items = ["Новая игра", "Продолжить", "Настройки", "Выход"]

    def select(self, item_name: str) -> str:
        """Возвращает текстовый результат выбора пункта."""

        return f"Выбран пункт: {item_name}"
