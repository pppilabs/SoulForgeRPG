"""Модуль игрового цикла.

Используется для демонстрации запуска и сопровождения основного
цикла обработки игровых событий в приложении SoulForgeRPG.
"""


class GameLoop:
    """Управляет состоянием выполнения игрового цикла.

    Attributes:
        target_fps: Целевая частота обновления кадров.
        running: Признак активности игрового цикла.
    """

    def __init__(self, target_fps: int = 60) -> None:
        """Создаёт контроллер игрового цикла.

        Args:
            target_fps: Желаемое количество кадров в секунду.
        """
        self.target_fps = target_fps
        self.running = False

    def start(self) -> None:
        """Запускает обработку игрового цикла."""

        self.running = True
