"""Модуль состояния героя и противников.

Определяет простую структуру данных для хранения боевых характеристик
персонажей, участвующих в игровом процессе.
"""

from dataclasses import dataclass


@dataclass
class CharacterState:
    """Хранит базовые характеристики игровой сущности.

    Attributes:
        name: Имя персонажа или противника.
        health: Текущее количество здоровья.
        stamina: Текущее количество выносливости.
        strength: Параметр силы, влияющий на урон.
        armor: Параметр брони, уменьшающий входящий урон.
    """

    name: str
    health: int
    stamina: int
    strength: int
    armor: int
