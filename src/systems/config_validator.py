"""Проверка корректности настроек проекта.

Содержит вспомогательные функции для валидации параметров запуска
и допустимости конфигурации окна приложения.
"""


def validate_resolution(width: int, height: int) -> bool:
    """Проверяет допустимость разрешения окна.

    Args:
        width: Ширина окна в пикселях.
        height: Высота окна в пикселях.

    Returns:
        True, если разрешение удовлетворяет минимальным требованиям.
    """

    return width >= 800 and height >= 600
